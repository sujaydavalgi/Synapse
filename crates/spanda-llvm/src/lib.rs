fn string_global_ptr_for(text: &str, strings: &[String]) -> String {
    // String global ptr for.
    //
    // Parameters:
    // - `text` — input value
    // - `strings` — input value
    //
    // Returns:
    // Text result.
    //
    // Options:
    // None.
    //
    // Example:
    // let result = spanda_llvm::string_global_ptr_for(text, strings);

    // Compute idx for the following logic.
    let idx = strings
        .iter()
        .position(|s| s == text)
        .unwrap_or_else(|| panic!("missing string literal `{text}`"));
    format!(
        "getelementptr inbounds ([{len} x i8], [{len} x i8]* {name}, i64 0, i64 0)",
        len = text.len() + 1,
        name = string_global_name(idx)
    )
}

fn string_global_def(idx: usize, text: &str) -> String {
    // String global def.
    //
    // Parameters:
    // - `idx` — input value
    // - `text` — input value
    //
    // Returns:
    // Text result.
    //
    // Options:
    // None.
    //
    // Example:
    // let result = spanda_llvm::string_global_def(idx, text);

    // Compute len for the following logic.
    let len = text.len() + 1;
    format!(
        "{name} = private unnamed_addr constant [{len} x i8] c\"{text}\\00\"\n",
        name = string_global_name(idx),
        len = len,
        text = escape_llvm_string(text)
    )
}

fn collect_actuator_globals(sir: &SirProgram, out: &mut String) {
    // Collect actuator globals.
    //
    // Parameters:
    // - `sir` — input value
    // - `out` — input value
    //
    // Returns:
    // Nothing.
    //
    // Options:
    // None.
    //
    // Example:
    // let result = spanda_llvm::collect_actuator_globals(sir, out);

    // Create mutable names for accumulating results.
    let mut names = Vec::new();

    // Handle each robot declared in the program.
    for robot in &sir.robots {
        // Process each behavior.
        for behavior in &robot.behaviors {
            collect_actuator_names(&behavior.body, &mut names);
        }
    }

    // Skip further work when names is empty.
    if names.is_empty() {
        return;
    }

    // Iterate over each name in names.
    for name in &names {
        out.push_str(&actuator_global_def(name));
    }
    out.push('\n');
}

fn collect_actuator_names(stmts: &[SirStmt], names: &mut Vec<String>) {
    // Collect actuator names.
    //
    // Parameters:
    // - `stmts` — input value
    // - `names` — input value
    //
    // Returns:
    // Nothing.
    //
    // Options:
    // None.
    //
    // Example:
    // let result = spanda_llvm::collect_actuator_names(stmts, names);

    // Execute each statement in sequence.
    for stmt in stmts {
        // Match on stmt and handle each case.
        match stmt {
            SirStmt::ActuatorDrive { actuator, .. } | SirStmt::ActuatorStop { actuator } => {
                // Check membership before continuing.
                if !names.contains(actuator) {
                    names.push(actuator.clone());
                }
            }
            SirStmt::IfBool {
                then_body,
                else_body,
                ..
            } => {
                collect_actuator_names(then_body, names);

                // Emit output when else body provides a else body.
                if let Some(else_body) = else_body {
                    collect_actuator_names(else_body, names);
                }
            }
            SirStmt::IfVar {
                then_body,
                else_body,
                ..
            } => {
                collect_actuator_names(then_body, names);

                // Emit output when else body provides a else body.
                if let Some(else_body) = else_body {
                    collect_actuator_names(else_body, names);
                }
            }
            SirStmt::MatchEnumUnit { arms, .. } => {
                // Process each arm.
                for arm in arms {
                    collect_actuator_names(&arm.body, names);
                }
            }
            SirStmt::LoopEvery { body, .. } => collect_actuator_names(body, names),
            _ => {}
        }
    }
}

