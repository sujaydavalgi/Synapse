fn escape_llvm_string(s: &str) -> String {
    // Escape llvm string.
    //
    // Parameters:
    // - `s` — input value
    //
    // Returns:
    // Text result.
    //
    // Options:
    // None.
    //
    // Example:
    // let result = spanda_llvm::escape_llvm_string(s);

    // Produce chars as the result.
    s.chars()
        .flat_map(|c| match c {
            '\\' => "\\5C".chars().collect::<Vec<_>>(),
            '"' => "\\22".chars().collect::<Vec<_>>(),
            '\n' => "\\0A".chars().collect::<Vec<_>>(),
            _ => vec![c],
        })
        .collect()
}

fn emit_behavior_fn(
    robot: &str,
    behavior: &SirBehavior,
    strings: &[String],
    hal: &HalEmitConfig,
) -> String {
    // Emit behavior fn.
    //
    // Parameters:
    // - `robot` — input value
    // - `behavior` — input value
    // - `strings` — input value
    // - `hal` — input value
    //
    // Returns:
    // Text result.
    //
    // Options:
    // None.
    //
    // Example:
    // let result = spanda_llvm::emit_behavior_fn(robot, behavior, strings, hal);

    // Create mutable loop id for accumulating results.
    let mut loop_id = 0usize;
    let mut branch_id = 0usize;
    let mut locals = EmitLocals::default();
    let mut body = emit_stmts(
        &behavior.body,
        "void",
        strings,
        &mut loop_id,
        &mut branch_id,
        &mut locals,
        hal,
    );

    // Check membership before continuing.
    if !body.contains("ret void") {
        body.push_str("  ret void\n");
    }
    format!(
        "define void @spanda_robot_{robot}_{}() {{\n{body}}}\n\n",
        behavior.name
    )
}

