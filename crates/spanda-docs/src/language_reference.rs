fn render_global_functions(out: &mut String) {
    // Description:
    //     Render global functions.
    //
    // Inputs:
    //     o: &mut String
    //         Caller-supplied o.
    //
    // Outputs:
    //     None.
    //
    // Example:

    //     let result = spanda_docs::language_reference::render_global_functions(o);

    out.push_str("## Global functions\n\n");
    out.push_str("Top-level functions available in every Spanda program.\n\n");

    let builtins = BUILTIN_FUNCTIONS();
    let mut names: Vec<_> = builtins.keys().copied().collect();
    names.sort_unstable();

    for name in names {
        let sig = builtins.get(name).expect("builtin fn");
        let mut named = BTreeMap::new();
        for (k, v) in sig.named_params() {
            named.insert(k.clone(), format_type_name(v));
        }
        let signature = format_fn_sig(name, &named, &format_type_name(sig.returns()));
        out.push_str(&format!("### `{name}` {{#fn-{name}}}\n\n"));
        out.push_str("```spanda\n");
        out.push_str(&signature);
        out.push_str("\n```\n\n");
        out.push_str(fn_description(name));
        out.push_str("\n\n");
    }
}

fn render_robot_methods(out: &mut String) {
    // Description:
    //     Render robot methods.
    //
    // Inputs:
    //     o: &mut String
    //         Caller-supplied o.
    //
    // Outputs:
    //     None.
    //
    // Example:

    //     let result = spanda_docs::language_reference::render_robot_methods(o);

    out.push_str("## Robot methods\n\n");
    out.push_str("Methods on the implicit `robot` receiver inside `behavior` blocks.\n\n");

    let robot_methods = ROBOT_METHODS();
    let mut names: Vec<_> = robot_methods.keys().copied().collect();
    names.sort_unstable();

    for name in names {
        let sig = robot_methods.get(name).expect("robot method");
        let (positional, named, returns) = collect_method_sig(sig);
        let signature = format_method_sig("robot", name, &positional, &named, &returns);
        out.push_str(&format!("### `robot.{name}` {{#robot-{name}}}\n\n"));
        out.push_str("```spanda\n");
        out.push_str(&signature);
        out.push_str("\n```\n\n");
        out.push_str(method_description("Robot", name));
        out.push_str("\n\n");
    }
}

fn render_type_methods(out: &mut String) {
    // Description:
    //     Render type methods.
    //
    // Inputs:
    //     o: &mut String
    //         Caller-supplied o.
    //
    // Outputs:
    //     None.
    //
    // Example:

    //     let result = spanda_docs::language_reference::render_type_methods(o);

    out.push_str("## Type methods\n\n");
    out.push_str(
        "Built-in methods on sensor, actuator, AI, safety, and twin types. \
         Actuator/sensor instances use the type declared in the robot graph.\n\n",
    );

    let methods = BUILTIN_METHODS();
    let mut type_names: Vec<_> = methods.keys().cloned().collect();
    type_names.sort_unstable();

    for type_name in type_names {
        let Some(type_methods) = methods.get(&type_name) else {
            continue;
        };
        out.push_str(&format!("### `{type_name}` {{#type-{type_name}}}\n\n"));

        let mut method_names: Vec<_> = type_methods.keys().cloned().collect();
        method_names.sort_unstable();

        for method_name in method_names {
            let sig = type_methods.get(&method_name).expect("method");
            let (positional, named, returns) = collect_method_sig(sig);
            let signature = format_method_sig(
                &type_name.to_lowercase(),
                &method_name,
                &positional,
                &named,
                &returns,
            );
            out.push_str(&format!(
                "#### `{method_name}` {{#type-{type_name}-{method_name}}}\n\n"
            ));
            out.push_str("```spanda\n");
            out.push_str(&signature);
            out.push_str("\n```\n\n");
            out.push_str(method_description(&type_name, &method_name));
            out.push_str("\n\n");
        }
    }
}

fn render_object_properties(out: &mut String) {
    // Description:
    //     Render object properties.
    //
    // Inputs:
    //     o: &mut String
    //         Caller-supplied o.
    //
    // Outputs:
    //     None.
    //
    // Example:

    //     let result = spanda_docs::language_reference::render_object_properties(o);

    out.push_str("## Object properties\n\n");
    out.push_str("Fields on structured runtime values returned by sensors and AI.\n\n");

    let props = OBJECT_PROPERTIES();
    let mut type_names: Vec<_> = props.keys().cloned().collect();
    type_names.sort_unstable();

    for type_name in type_names {
        let Some(fields) = props.get(&type_name) else {
            continue;
        };
        out.push_str(&format!("### `{type_name}`\n\n"));
        out.push_str("| Field | Type |\n|-------|------|\n");
        let mut field_names: Vec<_> = fields.keys().cloned().collect();
        field_names.sort_unstable();
        for field in field_names {
            let ty = fields.get(&field).expect("field type");
            out.push_str(&format!("| `{field}` | `{}` |\n", format_type_name(ty)));
        }
        out.push('\n');
    }
}

