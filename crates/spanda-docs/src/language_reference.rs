fn render_std_library(out: &mut String) {
    // Description:
    //     Render std library.
    //
    // Inputs:
    //     o: &mut String
    //         Caller-supplied o.
    //
    // Outputs:
    //     None.
    //
    // Example:

    //     let result = spanda_docs::language_reference::render_std_library(o);

    out.push_str("## Standard library (`std.*`)\n\n");
    out.push_str(
        "Import with `import std.robotics;` (or any namespace below). Types resolve with or \
         without the `std.<module>.` prefix.\n\n",
    );

    let mut namespaces: BTreeMap<&str, Vec<&str>> = BTreeMap::new();
    for (ns, types) in std_namespaces() {
        let entry = namespaces.entry(ns).or_default();
        for ty in types.iter().copied() {
            if !entry.contains(&ty) {
                entry.push(ty);
            }
        }
    }

    let type_methods = BUILTIN_METHODS();
    for (ns, types) in &namespaces {
        let anchor = ns.replace('.', "-");
        out.push_str(&format!("### `{ns}` {{#{anchor}}}\n\n"));
        out.push_str(namespace_summary(ns));
        out.push_str("\n\n");
        out.push_str("**Types**\n\n");
        for ty in types {
            if let Some(m) = type_methods.get(*ty) {
                out.push_str(&format!(
                    "- [`{ty}`](#type-{ty}) — {method_count} method(s)\n",
                    method_count = m.len()
                ));
            } else {
                out.push_str(&format!("- `{ty}`\n"));
            }
        }
        out.push('\n');
    }
}

fn format_named_params(named: &BTreeMap<String, String>) -> String {
    // Description:
    //     Format named params.
    //
    // Inputs:
    //     named: &BTreeMap<String, String>
    //         Caller-supplied named.
    //
    // Outputs:
    //     result: String
    //         Return value from `format_named_params`.
    //
    // Example:

    //     let result = spanda_docs::language_reference::format_named_params(named);

    named
        .iter()
        .map(|(k, v)| format!("{k}: {v}"))
        .collect::<Vec<_>>()
        .join(", ")
}

fn format_fn_sig(name: &str, named: &BTreeMap<String, String>, returns: &str) -> String {
    // Description:
    //     Format fn sig.
    //
    // Inputs:
    //     name: &str
    //         Caller-supplied name.
    //     named: &BTreeMap<String, String>
    //         Caller-supplied named.
    //     returns: &str
    //         Caller-supplied returns.
    //
    // Outputs:
    //     result: String
    //         Return value from `format_fn_sig`.
    //
    // Example:

    //     let result = spanda_docs::language_reference::format_fn_sig(name, named, returns);

    let params = format_named_params(named);
    if params.is_empty() {
        format!("fn {name}() -> {returns}")
    } else {
        format!("fn {name}({params}) -> {returns}")
    }
}

fn format_method_sig(
    receiver: &str,
    name: &str,
    positional: &[String],
    named: &BTreeMap<String, String>,
    returns: &str,
) -> String {
    // Description:
    //     Format method sig.
    //
    // Inputs:
    //     receiver: &str
    //         Caller-supplied receiver.
    //     name: &str
    //         Caller-supplied name.
    //     positional: &[String]
    //         Caller-supplied positional.
    //     named: &BTreeMap<String, String>
    //         Caller-supplied named.
    //     returns: &str
    //         Caller-supplied returns.
    //
    // Outputs:
    //     result: String
    //         Return value from `format_method_sig`.
    //
    // Example:

    //     let result = spanda_docs::language_reference::format_method_sig(receiver, name, positional, named, returns);

    let mut parts = positional.to_vec();
    for (k, v) in named {
        parts.push(format!("{k}: {v}"));
    }
    let params = parts.join(", ");
    format!("{receiver}.{name}({params}) -> {returns}")
}

fn collect_method_sig(method: &MethodSig) -> (Vec<String>, BTreeMap<String, String>, String) {
    // Description:
    //     Collect method sig.
    //
    // Inputs:
    //     ethod: &MethodSig
    //         Caller-supplied ethod.
    //
    // Outputs:
    //     result: (Vec<String>, BTreeMap<String, String>, String)
    //         Return value from `collect_method_sig`.
    //
    // Example:

    //     let result = spanda_docs::language_reference::collect_method_sig(ethod);

    let positional: Vec<String> = method.params().iter().map(format_type_name).collect();
    let named: BTreeMap<String, String> = method
        .named_params()
        .iter()
        .map(|(k, v)| (k.clone(), format_type_name(v)))
        .collect();
    (positional, named, format_type_name(method.returns()))
}

