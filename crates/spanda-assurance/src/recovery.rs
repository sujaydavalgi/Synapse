fn default_recovery_contexts(program: &Program) -> Vec<RecoveryContext> {
    let policies = extract_recovery_policies(program);
    if policies.is_empty() {
        return vec![RecoveryContext {
            issue: "gps.failed".into(),
            diagnosis: Some("Satellite lock lost".into()),
            classification: Some(FailureClassification::SensorFailure),
            level: RecoveryLevel::Level2AutomaticLowRisk,
        }];
    }
    policies
        .iter()
        .flat_map(|p| {
            p.triggers.iter().map(|(cond, _)| RecoveryContext {
                issue: cond.clone(),
                diagnosis: None,
                classification: None,
                level: RecoveryLevel::Level2AutomaticLowRisk,
            })
        })
        .collect()
}

fn infer_diagnosis(issue: &str, classification: FailureClassification) -> String {
    let lower = issue.to_lowercase();
    if lower.contains("gps") {
        "Satellite lock lost".into()
    } else if lower.contains("lidar") {
        "Lidar point cloud unavailable".into()
    } else if lower.contains("lte") || lower.contains("wifi") {
        "Connectivity link down".into()
    } else {
        match classification {
            FailureClassification::SensorFailure => "Sensor reading unavailable".into(),
            FailureClassification::ActuatorFailure => "Actuator response timeout".into(),
            FailureClassification::ConnectivityFailure => "Network unreachable".into(),
            FailureClassification::ProviderFailure => "Provider backend unavailable".into(),
            FailureClassification::PackageFailure => "Package load failure".into(),
            FailureClassification::MissionFailure => "Mission constraint violated".into(),
            FailureClassification::HealthDegradation => "Health check threshold exceeded".into(),
            FailureClassification::FleetFailure => "Fleet member unresponsive".into(),
            FailureClassification::SwarmFailure => "Swarm coordination lost".into(),
            FailureClassification::SafetyFailure => "Safety rule violation detected".into(),
            FailureClassification::Unknown => "Root cause under investigation".into(),
        }
    }
}

fn actions_from_policies(
    policies: &[RecoveryPolicySpec],
    issue: &str,
) -> Vec<PlannedRecoveryAction> {
    let lower = issue.to_lowercase();
    let mut actions = Vec::new();
    let mut order = 0u32;
    for policy in policies {
        for (condition, policy_actions) in &policy.triggers {
            if lower.contains(&condition.to_lowercase())
                || condition_matches(&lower, &condition.to_lowercase())
            {
                for action_text in policy_actions {
                    order += 1;
                    actions.push(parse_action_text(action_text, order));
                }
            }
        }
    }
    actions
}

fn condition_matches(issue: &str, condition: &str) -> bool {
    let parts: Vec<&str> = condition.split('.').collect();
    parts.iter().all(|p| issue.contains(p))
}

fn parse_action_text(text: &str, order: u32) -> PlannedRecoveryAction {
    let lower = text.to_lowercase();
    let (strategy, correction) = if lower.contains("visual_odometry") || lower.contains("switch_to")
    {
        (
            RecoveryStrategy::Failover,
            Some(SelfCorrectionAction::Custom {
                action: text.into(),
            }),
        )
    } else if lower.contains("reduce_speed") {
        (
            RecoveryStrategy::SpeedReduction,
            Some(SelfCorrectionAction::ReduceSpeed {
                limit: text.replace("reduce_speed", "").trim().into(),
            }),
        )
    } else if lower.contains("degraded") {
        (
            RecoveryStrategy::DegradedMode,
            Some(SelfCorrectionAction::EnterMode {
                mode: "DegradedMode".into(),
            }),
        )
    } else if lower.contains("safe_mode") || lower.contains("safe mode") {
        (
            RecoveryStrategy::DegradedMode,
            Some(SelfCorrectionAction::EnterMode {
                mode: "SafeMode".into(),
            }),
        )
    } else if lower.contains("reconnect") {
        (
            RecoveryStrategy::ProviderReconnect,
            Some(SelfCorrectionAction::ReconnectProvider {
                provider: "default".into(),
            }),
        )
    } else if lower.contains("pause") {
        (
            RecoveryStrategy::MissionPause,
            Some(SelfCorrectionAction::PauseMission),
        )
    } else {
        (
            RecoveryStrategy::Custom(text.into()),
            Some(SelfCorrectionAction::Custom {
                action: text.into(),
            }),
        )
    };
    let risk = if lower.contains("unsafe")
        || lower.contains("restart fleet")
        || lower.contains("open gate")
    {
        "High"
    } else if lower.contains("halt") || lower.contains("emergency") {
        "Critical"
    } else {
        "Low"
    };
    let requires_approval =
        risk == "High" || risk == "Critical" || lower.contains("resume mission");
    PlannedRecoveryAction {
        description: text.into(),
        strategy,
        correction,
        risk: risk.into(),
        requires_approval,
        order,
    }
}

fn default_actions_for_classification(
    classification: FailureClassification,
    issue: &str,
) -> Vec<PlannedRecoveryAction> {
    let defaults: &[&str] = match classification {
        FailureClassification::SensorFailure if issue.to_lowercase().contains("gps") => &[
            "switch_to visual_odometry",
            "reduce_speed 0.5 m/s",
            "enter degraded_mode",
        ],
        FailureClassification::SensorFailure => &["recalibrate sensor", "enter degraded_mode"],
        FailureClassification::ActuatorFailure => &["halt motion", "enter safe_mode"],
        FailureClassification::ConnectivityFailure => {
            &["restart connectivity", "enter degraded_mode"]
        }
        FailureClassification::ProviderFailure => &["reconnect provider", "use fallback provider"],
        FailureClassification::PackageFailure => &["reload package"],
        FailureClassification::MissionFailure => &["pause mission"],
        FailureClassification::HealthDegradation => {
            &["enter degraded_mode", "reduce_speed 0.3 m/s"]
        }
        FailureClassification::FleetFailure => &["reassign mission", "promote backup coordinator"],
        FailureClassification::SwarmFailure => &["redistribute tasks"],
        FailureClassification::SafetyFailure => &["enter safe_mode", "activate kill switch"],
        FailureClassification::Unknown => &["enter safe_mode"],
    };
    defaults
        .iter()
        .enumerate()
        .map(|(i, text)| parse_action_text(text, (i + 1) as u32))
        .collect()
}

fn infer_target_mode(actions: &[PlannedRecoveryAction]) -> Option<OperationalMode> {
    for action in actions {
        if let Some(SelfCorrectionAction::EnterMode { mode }) = &action.correction {
            return Some(match mode.to_lowercase().as_str() {
                "degradedmode" | "degraded_mode" | "degraded" => OperationalMode::DegradedMode,
                "safemode" | "safe_mode" | "safe" => OperationalMode::SafeMode,
                "emergencymode" | "emergency_mode" | "emergency" => OperationalMode::EmergencyMode,
                "recoverymode" | "recovery_mode" | "recovery" => OperationalMode::RecoveryMode,
                _ => OperationalMode::NormalMode,
            });
        }
    }
    None
}

fn assess_plan_risk(actions: &[PlannedRecoveryAction]) -> String {
    if actions
        .iter()
        .any(|a| a.risk.eq_ignore_ascii_case("critical"))
    {
        "Critical".into()
    } else if actions.iter().any(|a| a.risk.eq_ignore_ascii_case("high")) {
        "High".into()
    } else if actions
        .iter()
        .any(|a| a.risk.eq_ignore_ascii_case("medium"))
    {
        "Medium".into()
    } else {
        "Low".into()
    }
}

fn sanitize_name(s: &str) -> String {
    s.replace(['.', ' '], "_")
}

/// Validate mode transitions for recovery.
pub fn validate_mode_transitions(program: &Program) -> Vec<String> {
    let modes = extract_operating_modes(program);
    let mut issues = validate_modes(program);
    let has_recovery = modes.iter().any(|m| m.kind == ModeKind::Recovery);
    if !modes.is_empty() && !has_recovery {
        issues.push("No recovery mode declared".into());
    }
    issues
}

/// Parse self-correction action from text.
pub fn parse_self_correction(text: &str) -> SelfCorrectionAction {
    parse_action_text(text, 0)
        .correction
        .unwrap_or(SelfCorrectionAction::Custom {
            action: text.into(),
        })
}

/// Map operational modes from program declarations.
pub fn operational_modes_from_program(program: &Program) -> Vec<OperationalMode> {
    extract_operating_modes(program)
        .iter()
        .map(|m| match m.kind {
            ModeKind::Normal => OperationalMode::NormalMode,
            ModeKind::Degraded => OperationalMode::DegradedMode,
            ModeKind::Safe => OperationalMode::SafeMode,
            ModeKind::Emergency => OperationalMode::EmergencyMode,
            ModeKind::Recovery => OperationalMode::RecoveryMode,
        })
        .collect()
}

