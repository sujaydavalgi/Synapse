/// Input context for recovery planning.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct RecoveryContext {
    pub issue: String,
    pub diagnosis: Option<String>,
    pub classification: Option<FailureClassification>,
    pub level: RecoveryLevel,
}

/// Planner that generates recovery plans from failures and diagnoses.
pub struct RecoveryPlanner;

impl RecoveryPlanner {
    /// Generate a recovery plan for the given context and program.
    pub fn plan(program: &Program, context: &RecoveryContext) -> RecoveryPlan {
        let classification = context
            .classification
            .unwrap_or_else(|| classify_failure(&context.issue));
        let diagnosis = context
            .diagnosis
            .clone()
            .unwrap_or_else(|| infer_diagnosis(&context.issue, classification));
        let policies = extract_recovery_policies(program);
        let mut actions = actions_from_policies(&policies, &context.issue);
        if actions.is_empty() {
            actions = default_actions_for_classification(classification, &context.issue);
        }
        let target_mode = infer_target_mode(&actions);
        let risk = assess_plan_risk(&actions);
        let level = context.level;
        RecoveryPlan {
            name: format!("recovery_{}", sanitize_name(&context.issue)),
            failure: context.issue.clone(),
            classification,
            diagnosis,
            actions,
            target_mode,
            level,
            risk,
        }
    }
}

/// Classify a failure description into a failure category.
pub fn classify_failure(issue: &str) -> FailureClassification {
    let lower = issue.to_lowercase();
    if lower.contains("gps")
        || lower.contains("camera")
        || lower.contains("lidar")
        || lower.contains("sensor")
        || lower.contains("imu")
    {
        FailureClassification::SensorFailure
    } else if lower.contains("actuator") || lower.contains("motor") || lower.contains("drive") {
        FailureClassification::ActuatorFailure
    } else if lower.contains("lte")
        || lower.contains("wifi")
        || lower.contains("connect")
        || lower.contains("network")
    {
        FailureClassification::ConnectivityFailure
    } else if lower.contains("provider") {
        FailureClassification::ProviderFailure
    } else if lower.contains("package") {
        FailureClassification::PackageFailure
    } else if lower.contains("mission") {
        FailureClassification::MissionFailure
    } else if lower.contains("health") || lower.contains("degrad") {
        FailureClassification::HealthDegradation
    } else if lower.contains("fleet") {
        FailureClassification::FleetFailure
    } else if lower.contains("swarm") {
        FailureClassification::SwarmFailure
    } else if lower.contains("safety") || lower.contains("kill") {
        FailureClassification::SafetyFailure
    } else {
        FailureClassification::Unknown
    }
}

/// Extract recovery policies from program declarations.
pub fn extract_recovery_policies(program: &Program) -> Vec<RecoveryPolicySpec> {
    let Program::Program {
        recovery_policies,
        mitigations,
        ..
    } = program;

    let mut specs: Vec<RecoveryPolicySpec> = recovery_policies
        .iter()
        .map(|decl| {
            let RecoveryPolicyDecl::RecoveryPolicyDecl { name, branches, .. } = decl;
            RecoveryPolicySpec {
                name: name.clone(),
                triggers: branches
                    .iter()
                    .map(|b| (b.condition.clone(), b.actions.clone()))
                    .collect(),
            }
        })
        .collect();

    for mit in mitigations {
        let MitigationDecl::MitigationDecl { name, branches, .. } = mit;
        specs.push(RecoveryPolicySpec {
            name: name.clone(),
            triggers: branches
                .iter()
                .map(|b| (b.condition.clone(), b.actions.clone()))
                .collect(),
        });
    }

    specs
}

/// Validate recovery actions through safety, hardware, capability, and readiness gates.
pub fn validate_recovery_plan(program: &Program, plan: &RecoveryPlan) -> Vec<SafeRecoveryAction> {
    let options = ReadinessOptions {
        target: default_deploy_target(program),
        ..Default::default()
    };
    let readiness = evaluate_readiness(program, &options);
    let hw_options = VerifyOptions {
        target: default_deploy_target(program),
        ..Default::default()
    };
    let hw = verify_program_compatibility(program, &hw_options);
    let caps = infer_robot_capabilities(program);
    let approvals = verify_approvals(program);

    plan.actions
        .iter()
        .map(|action| {
            let safety = ValidationGateResult {
                gate: "Safety Validation".into(),
                passed: !action.description.to_lowercase().contains("unsafe")
                    && !plan
                        .classification
                        .eq(&FailureClassification::SafetyFailure),
                message: if action.description.to_lowercase().contains("unsafe") {
                    "Unsafe action blocked".into()
                } else {
                    "PASS".into()
                },
            };
            let hardware = ValidationGateResult {
                gate: "Hardware Verification".into(),
                passed: hw.compatible,
                message: if hw.compatible {
                    "PASS".into()
                } else {
                    "Hardware incompatible".into()
                },
            };
            let capability = ValidationGateResult {
                gate: "Capability Verification".into(),
                passed: !caps.is_empty()
                    || plan.classification == FailureClassification::ConnectivityFailure,
                message: if caps.is_empty() {
                    "No capabilities inferred".into()
                } else {
                    "PASS".into()
                },
            };
            let readiness_gate = ValidationGateResult {
                gate: "Readiness Validation".into(),
                passed: readiness.mission_ready
                    || (action.risk.eq_ignore_ascii_case("low") && readiness.score.total > 0)
                    || plan.level <= RecoveryLevel::Level1Recommend,
                message: if readiness.mission_ready {
                    "PASS".into()
                } else if action.risk.eq_ignore_ascii_case("low") && readiness.score.total > 0 {
                    format!("PASS (low-risk, score {}/100)", readiness.score.total)
                } else {
                    format!("Readiness score {}/100", readiness.score.total)
                },
            };
            let requires_approval = action.requires_approval
                || plan.level == RecoveryLevel::Level4HumanApproval
                || action.risk.eq_ignore_ascii_case("high")
                || action.risk.eq_ignore_ascii_case("critical");
            let approved = if requires_approval {
                approvals.compatible
                    && approvals.rows.iter().any(|r| {
                        r.approval_path_exists
                            && (r.actor == "Operator" || r.actor.contains("Operator"))
                    })
            } else {
                true
            };
            SafeRecoveryAction {
                action: action.clone(),
                safety_validation: safety,
                hardware_verification: hardware,
                capability_verification: capability,
                readiness_validation: readiness_gate,
                approved,
            }
        })
        .collect()
}

/// Evaluate whether recovery can be attempted.
pub fn evaluate_recovery_readiness(program: &Program, plan: &RecoveryPlan) -> RecoveryReadiness {
    let options = ReadinessOptions {
        target: default_deploy_target(program),
        ..Default::default()
    };
    let readiness = evaluate_readiness(program, &options);
    let safe_actions = validate_recovery_plan(program, plan);
    let all_gates_pass = safe_actions.iter().all(|a| {
        a.safety_validation.passed
            && a.hardware_verification.passed
            && a.capability_verification.passed
            && a.readiness_validation.passed
    });
    let mut blockers = Vec::new();
    if !all_gates_pass {
        blockers.push("One or more validation gates failed".into());
    }
    for action in &safe_actions {
        if action.action.requires_approval && !action.approved {
            blockers.push(format!(
                "Operator approval required for: {}",
                action.action.description
            ));
        }
    }
    let risk = plan.risk.clone();
    RecoveryReadiness {
        recovery_ready: all_gates_pass && blockers.is_empty(),
        risk,
        readiness_score: readiness.score.total,
        blockers,
    }
}

/// Execute recovery plan (static simulation — no uncontrolled autonomous behavior).
pub fn execute_recovery_plan(program: &Program, plan: &RecoveryPlan) -> RecoveryResult {
    let safe_actions = validate_recovery_plan(program, plan);
    let mut executed = Vec::new();
    let mut failed = Vec::new();

    for safe in &safe_actions {
        let gates_ok = safe.safety_validation.passed
            && safe.hardware_verification.passed
            && safe.capability_verification.passed
            && safe.readiness_validation.passed;
        if !gates_ok {
            failed.push(safe.action.description.clone());
            continue;
        }
        if safe.action.requires_approval && !safe.approved {
            failed.push(format!("{} (approval required)", safe.action.description));
            continue;
        }
        executed.push(safe.action.description.clone());
    }

    let status = if failed.is_empty() && !executed.is_empty() {
        RecoveryStatus::Success
    } else if !executed.is_empty() {
        RecoveryStatus::PartialSuccess
    } else if safe_actions.iter().any(|a| !a.safety_validation.passed) {
        RecoveryStatus::Unsafe
    } else {
        RecoveryStatus::Failed
    };

    let evidence = RecoveryEvidence {
        failure: plan.failure.clone(),
        diagnosis: plan.diagnosis.clone(),
        plan: plan.name.clone(),
        safety_validation: if safe_actions.iter().all(|a| a.safety_validation.passed) {
            "PASS".into()
        } else {
            "FAIL".into()
        },
        recovery_actions: executed.clone(),
        outcome: format!("{status:?}"),
        operator_approval: if plan.level == RecoveryLevel::Level4HumanApproval {
            Some("Operator".into())
        } else {
            None
        },
        verification: if status == RecoveryStatus::Success {
            "Recovery verified".into()
        } else {
            "Recovery incomplete".into()
        },
    };

    RecoveryResult {
        plan: plan.name.clone(),
        status,
        executed_actions: executed,
        failed_actions: failed,
        verification_outcome: evidence.verification.clone(),
        evidence,
    }
}

/// Build recovery audit records from results.
pub fn build_recovery_audit(results: &[RecoveryResult]) -> Vec<RecoveryAuditRecord> {
    results
        .iter()
        .map(|r| RecoveryAuditRecord {
            detected_failure: r.evidence.failure.clone(),
            diagnosis: r.evidence.diagnosis.clone(),
            chosen_recovery: r.evidence.plan.clone(),
            safety_validation_result: r.evidence.safety_validation.clone(),
            recovery_outcome: format!("{:?}", r.status),
            operator_approval: r.evidence.operator_approval.clone(),
            verification_outcome: r.verification_outcome.clone(),
            timestamp_hint: "static-analysis".into(),
        })
        .collect()
}

/// Build traceability chain from recovery results.
pub fn build_recovery_traceability(results: &[RecoveryResult]) -> Vec<RecoveryTraceChain> {
    results
        .iter()
        .map(|r| RecoveryTraceChain {
            failure: r.evidence.failure.clone(),
            diagnosis: r.evidence.diagnosis.clone(),
            recovery_plan: r.evidence.plan.clone(),
            safety_validation: r.evidence.safety_validation.clone(),
            recovery_action: r.executed_actions.join("; "),
            outcome: format!("{:?}", r.status),
            evidence: r.evidence.verification.clone(),
        })
        .collect()
}

/// Plan fleet recovery actions.
pub fn plan_fleet_recovery(program: &Program, failed_robot: &str) -> Vec<FleetRecoveryPlan> {
    let Program::Program { fleets, .. } = program;
    fleets
        .iter()
        .map(|f| {
            let spanda_ast::robotics_decl::FleetDecl::FleetDecl { name, .. } = f;
            FleetRecoveryPlan {
                fleet: name.clone(),
                failed_robot: Some(failed_robot.into()),
                actions: vec![
                    format!("Replace failed robot {failed_robot}"),
                    "Reassign mission to backup robot".into(),
                    "Redistribute tasks across fleet".into(),
                    "Promote backup coordinator".into(),
                ],
                risk: "Medium".into(),
            }
        })
        .collect()
}

/// Build recovery knowledge base from historical patterns.
pub fn build_recovery_knowledge(program: &Program) -> RecoveryKnowledgeBase {
    let failure_report = analyze_failure(program);
    let entries: Vec<RecoveryKnowledgeEntry> = failure_report
        .impacts
        .iter()
        .map(|impact| RecoveryKnowledgeEntry {
            failure_pattern: impact.component.clone(),
            recovery_pattern: impact.mitigation.clone(),
            success_rate: match impact.severity.as_str() {
                "Critical" => 0.6,
                "High" => 0.75,
                "Medium" => 0.85,
                _ => 0.9,
            },
            recommendation: format!("On {} failure: {}", impact.component, impact.mitigation),
        })
        .collect();
    RecoveryKnowledgeBase { entries }
}

/// Simulate failure injection for recovery testing.
pub fn simulate_failure_recovery(program: &Program, failure_kind: &str) -> RecoveryReport {
    let context = RecoveryContext {
        issue: format!("{failure_kind} failure"),
        diagnosis: Some(infer_diagnosis(
            failure_kind,
            classify_failure(failure_kind),
        )),
        classification: Some(classify_failure(failure_kind)),
        level: RecoveryLevel::Level3AutomaticWithValidation,
    };
    evaluate_recovery(program, Some(&context))
}

/// Run full recovery framework analysis on a program.
pub fn evaluate_recovery(program: &Program, context: Option<&RecoveryContext>) -> RecoveryReport {
    let policies = extract_recovery_policies(program);
    let contexts: Vec<RecoveryContext> = if let Some(ctx) = context {
        vec![ctx.clone()]
    } else {
        default_recovery_contexts(program)
    };

    let plans: Vec<RecoveryPlan> = contexts
        .iter()
        .map(|ctx| RecoveryPlanner::plan(program, ctx))
        .collect();

    let safe_actions: Vec<SafeRecoveryAction> = plans
        .iter()
        .flat_map(|plan| validate_recovery_plan(program, plan))
        .collect();

    let results: Vec<RecoveryResult> = plans
        .iter()
        .map(|plan| execute_recovery_plan(program, plan))
        .collect();

    let audit = build_recovery_audit(&results);
    let traceability = build_recovery_traceability(&results);
    let readiness = plans
        .first()
        .map(|p| evaluate_recovery_readiness(program, p))
        .unwrap_or(RecoveryReadiness {
            recovery_ready: false,
            risk: "Unknown".into(),
            readiness_score: 0,
            blockers: vec!["No recovery plan".into()],
        });

    let success_count = results
        .iter()
        .filter(|r| r.status == RecoveryStatus::Success)
        .count();
    let success_rate = if results.is_empty() {
        0.0
    } else {
        success_count as f64 / results.len() as f64
    };

    let assurance = RecoveryAssuranceMetrics {
        recovery_evidence: results.iter().map(|r| r.evidence.clone()).collect(),
        recovery_readiness: readiness.clone(),
        success_rate,
        traceability,
    };

    let fleet_plans = plan_fleet_recovery(program, "Rover");
    let knowledge = build_recovery_knowledge(program);
    let mode_issues = validate_modes(program);

    let passed = !plans.is_empty()
        && results
            .iter()
            .all(|r| r.status != RecoveryStatus::Unsafe && r.status != RecoveryStatus::Failed)
        && mode_issues.is_empty();

    RecoveryReport {
        policies,
        plans,
        safe_actions,
        results,
        audit,
        readiness,
        assurance,
        fleet_plans,
        knowledge,
        passed,
    }
}

/// Integrate recovery with diagnosis report.
pub fn recovery_from_diagnosis(program: &Program, diagnosis: &DiagnosisReport) -> RecoveryReport {
    let mut contexts = Vec::new();
    for d in &diagnosis.static_diagnoses {
        for rc in &d.root_causes {
            contexts.push(RecoveryContext {
                issue: rc.description.clone(),
                diagnosis: Some(d.subject.clone()),
                classification: Some(classify_failure(&rc.description)),
                level: RecoveryLevel::Level2AutomaticLowRisk,
            });
        }
    }
    if let Some(trace) = &diagnosis.trace_diagnosis {
        contexts.push(RecoveryContext {
            issue: trace.root_cause.clone(),
            diagnosis: Some(trace.root_cause.clone()),
            classification: Some(classify_failure(&trace.root_cause)),
            level: RecoveryLevel::Level2AutomaticLowRisk,
        });
    }
    if contexts.is_empty() {
        return evaluate_recovery(program, None);
    }
    let policies = extract_recovery_policies(program);
    let plans: Vec<RecoveryPlan> = contexts
        .iter()
        .map(|ctx| RecoveryPlanner::plan(program, ctx))
        .collect();
    let results: Vec<RecoveryResult> = plans
        .iter()
        .map(|plan| execute_recovery_plan(program, plan))
        .collect();
    let safe_actions: Vec<SafeRecoveryAction> = plans
        .iter()
        .flat_map(|plan| validate_recovery_plan(program, plan))
        .collect();
    let audit = build_recovery_audit(&results);
    let traceability = build_recovery_traceability(&results);
    let readiness = plans
        .first()
        .map(|p| evaluate_recovery_readiness(program, p))
        .unwrap_or(RecoveryReadiness {
            recovery_ready: false,
            risk: "Unknown".into(),
            readiness_score: 0,
            blockers: vec!["No recovery plan".into()],
        });
    let success_count = results
        .iter()
        .filter(|r| r.status == RecoveryStatus::Success)
        .count();
    let success_rate = if results.is_empty() {
        0.0
    } else {
        success_count as f64 / results.len() as f64
    };
    let recovery_evidence: Vec<RecoveryEvidence> =
        results.iter().map(|r| r.evidence.clone()).collect();
    let passed = !results.is_empty() && results.iter().all(|r| r.status != RecoveryStatus::Unsafe);
    RecoveryReport {
        policies,
        plans,
        safe_actions,
        results,
        audit,
        readiness: readiness.clone(),
        assurance: RecoveryAssuranceMetrics {
            recovery_evidence,
            recovery_readiness: readiness,
            success_rate,
            traceability,
        },
        fleet_plans: plan_fleet_recovery(program, "Rover"),
        knowledge: build_recovery_knowledge(program),
        passed,
    }
}

